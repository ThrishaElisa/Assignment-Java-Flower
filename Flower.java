public class Flower
{
    //declare attributes for the flower class such as name, color, price and quantity and make it private
    private String name;
    private String color;
    private double price;
    private int quantity;

    
    //public constructor without parameter
    public Flower()
    {
        name = "";
        color = "";
        price = 0.0;
        quantity = 0;
        
    }
    
    //public constructor with parameter, to assign values in class attributes
    public Flower(String name, String color, double price, int quantity)
    {
        this.name = name;
        this.color = color;
        this.price = price;
        this.quantity = quantity;
        
    }
    
    //mutators for the Flower class, set all attributes
    public void setFlower(String name, String color, double price, int quantity)
    {
        this.name = name;
        this.color = color;
        this.price = price;
        this.quantity = quantity;
        
    }    
    
    //mutator to set name
    public void setName(String name)
    {
        this.name = name;
        
    }
    
    //mutator to set color
    public void setColor(String color)
    {
        this.color = color;
        
    }
    
    //mutator to set price
    public void setPrice(double price)
    {
        this.price = price;
        
    }
    
    //mutator to set quantity
    public void setQuantity(int quantity)
    {
        this.quantity = quantity;
        
    }
    
    //accessor to get name
    public String getName()
    {
        return name;
        
    }
    
    //accessor to get color
    public String getColor()
    {
        return color;
        
    }
    
    //accessor to get price
    public double getPrice()
    {
        return price;
        
    }
    
    //accessor to get quantity
    public int getQuantity()
    {
        return quantity;
        
    }
    
    //a toString() method to display flower details, void since will display in the method itself
    public String toString(){
       return "Name: "+name+"\nColor: "+color+"\nPrice: "+price+"\nQuantity: "+quantity+"\n";
    }
    
    

    
}
